package mz.co.lunguissa.entity;

public class Idade 
{
	private int codigoIdade;
	private String descricao;
	private int minDias;
	private int maxDias;
	public int getCodigoIdade() {
		return codigoIdade;
	}
	public void setCodigoIdade(int codigoIdade) {
		this.codigoIdade = codigoIdade;
	}
	public String getDescricao() {
		return descricao;
	}
	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}
	public int getMinDias() {
		return minDias;
	}
	public void setMinDias(int minDias) {
		this.minDias = minDias;
	}
	public int getMaxDias() {
		return maxDias;
	}
	public void setMaxDias(int maxDias) {
		this.maxDias = maxDias;
	}
	
	
}
